        	<h2>A propos de l'application ERIL</h2>
			
			<p>La détermination de la rentabilité d'un investissement (rendement locatif) est un élément majeur 
			dans les décisions que va prendre un investisseur.
			<br>
			<b>Un outil informatique performant et pertinent est donc indispendable</b><br>
			C'est dans ce but que l'application ERIL a été conçue par Jérôme BRIANNE 
			(investisseur expérimenté dans les projets locatifs).<br>
			Initialement développée sous Excel (ERIL 1.0), puis en VBA (ERIL 2.0), 
			ERIL a été ensuite été reprogrammé en logiciel Windows (ERIL 3.0) afin d'optimiser les performances et 
			les capacités de calculs.<br>
			<br>
			Pour pouvoir encore accroitre les capacités de cet outil et afin de permettre son utilisation sur tout 
			type de supports, il s'est avéré nécessaire de convertir ERIL en application Web<br>
			<br>
			Michel DRIANT (consultant informatique) s'est donc associé au projet ERIL afin d'assurer que cette 
			nouvelle mutation soit parfaitement orchestrée</p>
